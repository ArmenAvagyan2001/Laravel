     * Handle the User "updated" event.
     *
     * @param  \App\Models\User  $user
     * @return void
     */
    public function updated(User $user)
    {
        if ($user->isDirty('email')){
            Mail::to($user)->send(new UserRegistrationMail($user));
            $user->email_verified_at = null;
        }
    }

